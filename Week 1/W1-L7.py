print("s is of type :" ,type(s))

l = [10, 20, 30, 40, 50]
print(l)
print(l[0])
print(l[-1])    # Negative indexing - starts from the end, prints the last element
print(l[1:3])   # Slicing - prints elements from index 1 to 2
print(type(l))

